public class Metric {
  public static double cmToInch(double cm){
    return cm * .39370;
  }
  
  public static double inchToCm(double inch){
    return inch / .39370;
  }
  
  public static double kmToMiles(double km){
    return km * .62137;
  }
  
  public static double milesToKm(double miles){
    return miles / .621;
  }


}